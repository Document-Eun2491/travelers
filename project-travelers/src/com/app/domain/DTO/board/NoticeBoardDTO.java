package com.app.domain.DTO.board;

public class NoticeBoardDTO {
	private Long id;
	private String title;
	private String content;
	private String registerDate;
	private String updatedDate;
	private String viewedCount;
	private Long adminId;
	private String adminNickname;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRegisterDate() {
		return registerDate;
	}
	public void setRegisterDate(String registerDate) {
		this.registerDate = registerDate;
	}
	public String getUpdatedDate() {
		return updatedDate;
	}
	public void setUpdatedDate(String updatedDate) {
		this.updatedDate = updatedDate;
	}
	public String getViewedCount() {
		return viewedCount;
	}
	public void setViewedCount(String viewedCount) {
		this.viewedCount = viewedCount;
	}
	public Long getAdminId() {
		return adminId;
	}
	public void setAdminId(Long adminId) {
		this.adminId = adminId;
	}
	public String getAdminNickname() {
		return adminNickname;
	}
	public void setAdminNickname(String adminNickname) {
		this.adminNickname = adminNickname;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		NoticeBoardDTO other = (NoticeBoardDTO) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	@Override
	public String toString() {
		return "NoticeBoardDTO [id=" + id + ", title=" + title + ", content=" + content + ", registerDate="
				+ registerDate + ", updatedDate=" + updatedDate + ", viewedCount=" + viewedCount + ", adminId="
				+ adminId + ", adminNickname=" + adminNickname + "]";
	}
	
	
}
